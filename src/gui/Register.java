    private void button_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_registerActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String namadepan;
        String namabelakang;
        Boolean gender;
        String nomortelepon;
        String email;
        String username;
        String password;
        
        String gethari = cb_date.getSelectedItem().toString();
        String getbulan = cb_datemonth.getSelectedItem().toString();
        String gettahun = cb_dateyear.getSelectedItem().toString();
        int hari = Integer.parseInt(gethari);
        int bulan = Integer.parseInt(getbulan);
        int tahun = Integer.parseInt(gettahun);
        String birthdate = String.format("%d-%d-%d", tahun, bulan, hari);
        
        if (field_password.getText().equals(field_repassword.getText())){
            namadepan = field_namadepan.getText();
            namabelakang = field_namabelakang.getText();
            if(rdbtn_laki.isSelected()) {
		gender = true;
            } else {
		gender = false;
            }
            nomortelepon = field_nomortelepon.getText();
            email = field_email.getText();
            username = field_username.getText();
            password = field_password.getText();
            
            User newUser = new User(namadepan, namabelakang, birthdate, gender, nomortelepon, email, username, password);
            newUser.input_biodata();
            
            Login loginpage = new Login();
            loginpage.show();
            
            dispose();
        } else {
            label_passwordharussama.setText("password harus sama!");
        }
    }//GEN-LAST:event_button_registerActionPerformed

